import Foundation

final class Calculator {
    func add(_ a: Double, _ b: Double) -> Double {
        return a + b
    }

    func minusOperation(_ a: Double, _ b: Double) -> Double {
        return a - b
    }

    func multiply(_ a: Double, _ b: Double) -> Double {
        return a * b
    }

    func divide(_ a: Double, _ b: Double) -> Double {
        return a / b
    }
}
